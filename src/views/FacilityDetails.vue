  addCircleOutline,
  addOutline,
  closeCircleOutline,
  closeOutline,
  ellipsisVerticalOutline,
  globeOutline,
  locationOutline,
  openOutline,
  personOutline
} from 'ionicons/icons'
import { translate } from '@hotwax/dxp-components';
import FacilityMappingPopover from '@/components/FacilityMappingPopover.vue'
